package com.gr.griep.powop.util;

public class Vector2i {

	private int x;
	private int z;
	
	public Vector2i(int x, int z) {
		this.x = x;
		this.z = z;
	}
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getZ() {
		return z;
	}
	public void setZ(int z) {
		this.z = z;
	}	
}
